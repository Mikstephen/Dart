void main(){
  // Data Types
  // Numbers, Strings, Booleans, Lists, Maps, Dynamic

  // Strings
  String name = "John";
  print("String: $name");
  // Integers
  int age = 25;
  print("Integer: $age");
  // Doubles are floating-point numbers (decimals)
  double height = 1.75;
  print("Double: $height");
  // Booleans
  bool isStudent = true;
  print("Boolean: $isStudent");
  // Dynamic
  dynamic fullname = "Stephen Oluwakorede";
  print("Dynamic: $fullname");
}